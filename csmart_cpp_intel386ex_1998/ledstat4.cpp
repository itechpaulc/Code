//////////////////////////////////////////////////
//////////////////////////////////////////////////

WORD
HeadCmdCommLedStatusMachine::HCC_SLM_h1(void)
{
        LedOff();

        StartTimer(MSEC(50));

    return  HCC_SLM_OFF;
}

//////////////////////////////////////////////////
//
//
//
//////////////////////////////////////////////////

WORD
HeadCmdCommLedStatusMachine::HCC_SLM_h2(void)
{
        LedOn();

        StartTimer(MSEC(50));

    return  HCC_SLM_ON;
}
