void    Init_HeartBeatHandler(void)
{
	SendMessage(HeartBeatHandler, GoActive );
}

////////////////////////////////////////////////////////////////////
//
// Exit Procedures
//
////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////
//
// GoActive
//
////////////////////////////////////////////////////////////////////

NEW_STATE   HBH_exitA(void)
{
    //print("HBH ACTIVE\n");

    //HEART_BEAT_OFF();

    StartTimer(HEARTBEAT_UPDATE_RATE);

	SPI_SET_READY_LED( OFF );

    return HBH_ACTIVE_OFF;
}


////////////////////////////////////////////////////////////////////
//
// TimeOut while in HBH_ACTIVE_OFF
//
////////////////////////////////////////////////////////////////////

NEW_STATE   HBH_exitB(void)
{
    //HEART_BEAT_ON();

    StartTimer(HEARTBEAT_UPDATE_RATE);
   	SPI_SET_READY_LED( ON );

    return HBH_ACTIVE_ON;
}

////////////////////////////////////////////////////////////////////
//
// TimeOut while in HBH_ACTIVE_ON
//
////////////////////////////////////////////////////////////////////

NEW_STATE   HBH_exitC(void)
{
    StartTimer(HEARTBEAT_UPDATE_RATE);
	SPI_SET_READY_LED( OFF );

    return HBH_ACTIVE_OFF;
}


////////////////////////////////////////////////////////////////////
//
// TimeOut while in HBH_ACTIVE_TEST
//
////////////////////////////////////////////////////////////////////

NEW_STATE   HBH_exitD(void)
{
  //  HEART_BEAT_OFF();

    StartTimer(HEARTBEAT_UPDATE_RATE);

    return HBH_ACTIVE_ON;
}


////////////////////////////////////////////////////////////////////
//
// State Matrix Tables
